public class UnicodeExample {
    public static void main(String[] args) {
        // Store a Unicode character (for example, the character 'A' with Unicode \u0041)
        char unicodeChar = '\u0041';  // Unicode for character 'A'

        // Display the character and its corresponding value
        System.out.println("The Unicode character is: " + unicodeChar);
        System.out.println("The Unicode value is: " + (int) unicodeChar);  // Display its integer value
    }
}

