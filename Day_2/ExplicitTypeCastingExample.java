public class ExplicitTypeCastingExample {
    public static void main(String[] args) {
        double doubleValue = 123.456789; // A double value
        float floatValue = (float) doubleValue; // Explicitly casting double to float

        System.out.println("Double value: " + doubleValue);
        System.out.println("Float value after casting: " + floatValue);
    }
}
