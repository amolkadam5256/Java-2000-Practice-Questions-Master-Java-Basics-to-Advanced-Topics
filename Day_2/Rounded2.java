public class Rounded2 {
    public static void main(String[] args){
                // Declare three decimal numbers
                double num1 = 12.345;
                double num2 = 8.567;
                double num3 = 4.789;

                // Calculate the sum
                double sum = num1 + num2 + num3;

                // Print the result rounded to 2 decimal places
                System.out.printf("The sum is: %.2f",sum);

    }
}
